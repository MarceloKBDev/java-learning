package exerciseListRepetitionStatements;

public class Exercise1 {
/*
 * Write an algorithm that displays the message 'I like studying Algorithms!' 20 times.
 */
	public static void main(String[] args) {
		int i = 1;
		
		while (i<=20) {
			System.out.println("I like studying Algorithms!");
			i++;
		}
		
	}
}